#include "fdf.h"

int		width(t_point *point)
{
	t_point	*tmp;
	int		i;

	i = 0;
	tmp = point;
	while (tmp && tmp->y == 0)
	{
		++i;
		tmp = tmp->next;
	}
	return (i);
}
